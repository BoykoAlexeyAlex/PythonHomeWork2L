s=input('Введите строку ')
print('Третий символ : ',s[2])
print('Предпоследний символ: ',s[-2])
print('Первые пять символов: ',s[:5])
print('Без последних двух символов',s[:-2])
print('Символы с четными индексами',s[::2])
print('Символы с нечетными индексами',s[1::2])
print('Символы в обратном порядке',s[::-1])
print('Символы в обратном порядке через один',s[::-2])
print('Длина строки: ',len(s))